    public static int modCalculate( int aP ) {
        if (aP < 10) {
            if ( aP % 2 == 0) {
                return (int)Math.floor((aP - 10) / 2);
            }
            return (int)Math.floor((aP - 10) / 2) - 1;
        }
        else {
            return (int)Math.floor((aP - 10) / 2); 
        }
    }
    
